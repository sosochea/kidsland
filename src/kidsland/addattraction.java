private void selectImage() {
        JFileChooser fileChooser = new JFileChooser();
        int returnValue = fileChooser.showOpenDialog(this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            selectedImagePath = selectedFile.getAbsolutePath();
            JOptionPane.showMessageDialog(this, "Image selected: " + selectedFile.getName());
        }
    }

    private void addAttraction() {
        String name = jTextField1.getText();
        String quantity = jTextField2.getText();
        String price = jTextField3.getText();


        if (name.isEmpty() || quantity.isEmpty() || selectedImagePath == null) {
            JOptionPane.showMessageDialog(this, "Please fill all fields and select an image.");
            return;
        }

        // Insert into database
        try {
            Connection conn = Mysqlc.mycon();
            String query = "INSERT INTO ride (nom,Quantit√©, photo,prix) VALUES (?, ?, ?,?)";
            PreparedStatement pstmt = conn.prepareStatement(query);
            pstmt.setString(1, name);
            pstmt.setInt(2, Integer.parseInt(quantity));
            pstmt.setBytes(3, loadImageBytes(selectedImagePath));
            pstmt.setInt(4, Integer.parseInt(price));


            pstmt.executeUpdate();
            pstmt.close();
            conn.close();
            JOptionPane.showMessageDialog(this, "Attraction added successfully!");
            clearFields();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error adding attraction: " + e.getMessage());
        }
    }

    private byte[] loadImageBytes(String imagePath) {
        try (InputStream is = new FileInputStream(imagePath)) {
            return is.readAllBytes();
        } catch (IOException e) {
            e.printStackTrace();
            return null;
        }
    }

    private void clearFields() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");

        selectedImagePath = null;
    }

